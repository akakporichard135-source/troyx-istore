/**
 * Get product images with fallback
 */
export function getProductImages(productId: string): string[] {
  const images = productImages[productId as keyof typeof productImages];
  if (images) return images;
  
  // Fallback to generic Apple product image
  return [
    "https://images.unsplash.com/photo-1592286927505-1fed6c6d6b97?w=1000&q=80",
    "https://images.unsplash.com/photo-1611532736579-6b16e2b50449?w=1000&q=80"
  ];
}

/**
 * Get featured product images for homepage
 */
export function getFeaturedImage(productId: string): string {
  const images = getProductImages(productId);
  return images[0];
}

